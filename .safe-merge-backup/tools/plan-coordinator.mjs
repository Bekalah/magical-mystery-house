import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const BASE_DIR = path.join(__dirname, '..');

const STATE_FILE = path.join(BASE_DIR, '.continuous-improvement-state.json');
const LOCK_FILE = path.join(BASE_DIR, '.continuous-improvement-state.lock');

/**
 * Check if experiment is currently saving state
 */
export function isExperimentSaving() {
  return fs.existsSync(LOCK_FILE);
}

/**
 * Wait for experiment save to complete (with timeout)
 */
export async function waitForExperimentSave(timeout = 5000) {
  const start = Date.now();
  while (fs.existsSync(LOCK_FILE) && (Date.now() - start) < timeout) {
    await new Promise(resolve => setTimeout(resolve, 100));
  }
  return !fs.existsSync(LOCK_FILE);
}

/**
 * Get read-only access to state file
 */
export function readStateSafely() {
  try {
    if (!fs.existsSync(STATE_FILE)) {
      return null;
    }
    
    // Try to read, but don't wait if locked
    if (fs.existsSync(LOCK_FILE)) {
      // Check if lock is stale (older than 10 seconds)
      const lockStats = fs.statSync(LOCK_FILE);
      const lockAge = Date.now() - lockStats.mtimeMs;
      if (lockAge > 10000) {
        // Stale lock, safe to read
        return JSON.parse(fs.readFileSync(STATE_FILE, 'utf-8'));
      }
      // Active lock, return null to indicate busy
      return null;
    }
    
    // No lock, safe to read
    return JSON.parse(fs.readFileSync(STATE_FILE, 'utf-8'));
  } catch (error) {
    return null;
  }
}

/**
 * Check if plan creation can proceed safely
 */
export async function canCreatePlan() {
  // Wait briefly for any active save
  const saveComplete = await waitForExperimentSave(2000);
  
  if (!saveComplete) {
    // Experiment is saving, but we can still proceed with read-only access
    return { canProceed: true, readOnly: true };
  }
  
  return { canProceed: true, readOnly: false };
}

